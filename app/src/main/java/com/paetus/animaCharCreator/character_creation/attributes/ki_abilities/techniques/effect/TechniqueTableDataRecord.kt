    fun findData(
        name: Int,
        primary: Int,
        secondary: Int,
        mkCost: Int
    ): TechniqueTableData{
        val searchTable = when(name){
            1 -> table1
            2 -> table2
            3 -> table3
            4 -> table4
            5 -> table5
            6 -> table6
            7 -> table7
            8 -> if(primary != secondary) table8 else table8a
            9 -> {
                if(primary != secondary) table9
                else if (primary == 10) table9a
                else table9b
            }
            10 -> if(primary != secondary) table10 else table10a
            11 -> if(primary != secondary) table11 else table11a
            12 -> if(primary != secondary) table12 else table12a
            13 -> table13
            15 -> table15
            16 -> table16
            17 -> table17
            18 -> table18
            19 -> table19
            20 -> table20
            21 -> if(primary != secondary) table21 else table21a
            22 -> table22
            23 -> if(primary != secondary) table23 else table23a
            24 -> if(primary != secondary) table24 else table24a
            25 -> table25
            26 -> table26
            27 -> table27
            28 -> table28
            29 -> if(primary != secondary) table29 else table29a
            30 -> {
                if(primary != secondary) table30
                else if (primary == 2) table30a
                else table30b
            }
            31 -> table31
            32 -> table32
            33 -> table33
            34 -> table34
            35 -> table35
            36 -> table36
            37 -> table37
            38 -> table38
            else -> null
        }

        var output: TechniqueTableData = table37[0]

        searchTable!!.forEach{
            if(it.primaryCost == primary &&
                    it.secondaryCost == secondary &&
                    it.mkCost == mkCost)
                output = it
        }

        if(name == 37)
            output = TechniqueTableData(
                name,
                output.effectRef,
                output.effectVal,
                output.primaryCost,
                output.secondaryCost,
                mkCost,
                output.maintCost,
                output.level
            )

        return output
    }

    fun stateEffect(index: Int, costRemain: Int, primary: Int): TechniqueTableData?{
        val base = table14a[index]

        table14.forEach{
            if(it.mkCost == costRemain && it.primaryCost == primary){
                return TechniqueTableData(
                    14,
                    base.effectRef,
                    it.effectVal,
                    base.primaryCost + it.primaryCost,
                    base.secondaryCost + it.secondaryCost,
                    base.mkCost + it.mkCost,
                    it.maintCost,
                    if(base.level >= it.level) base.level else it.level
                )
            }
        }

        return null
    }
