/**
 * Format of how technique effect data is stored.
 *
 * @param effectRef name to display for the data
 * @param primaryCost ki cost of technique effect if selected first
 * @param secondaryCost ki cost of technique effect if not selected first
 * @param mkCost martial knowledge required to use effect
 * @param maintCost ki cost to maintain the effect
 * @param level minimum technique level needed to acquire the effect
 */
data class TechniqueTableData(
    val name: Int,
    val effectRef: Int,
    val effectVal: Int?,
    val primaryCost: Int,
    val secondaryCost: Int,
    val mkCost: Int,
    val maintCost: Int,
    val level: Int
){
    fun isEquivalent(input: TechniqueTableData): Boolean{
        return input.name == name &&
                input.effectRef == effectRef &&
                input.effectVal == effectVal &&
                input.primaryCost == primaryCost &&
                input.secondaryCost == secondaryCost &&
                input.mkCost == mkCost &&
                input.maintCost == maintCost &&
                input.level == level
    }

    fun write(charInstance: BaseCharacter){
        charInstance.addNewData(name)

        if(name == 14){
            when(effectRef){
                R.string.physReduction -> {
                    charInstance.addNewData(0)
                    charInstance.addNewData(mkCost - 10)
                }
                R.string.blindnessData ->{
                    charInstance.addNewData(1)
                    charInstance.addNewData(mkCost - 15)
                }
                R.string.charReduction ->{
                    charInstance.addNewData(2)
                    charInstance.addNewData(mkCost - 10)
                }
                R.string.partParalyze ->{
                    charInstance.addNewData(3)
                    charInstance.addNewData(mkCost - 10)
                }
                R.string.damage ->{
                    charInstance.addNewData(4)
                    charInstance.addNewData(mkCost - 10)
                }
                R.string.unconscious ->{
                    charInstance.addNewData(5)
                    charInstance.addNewData(mkCost - 15)
                }
                R.string.coma ->{
                    charInstance.addNewData(6)
                    charInstance.addNewData(mkCost - 30)
                }
                R.string.totalParalyze ->{
                    charInstance.addNewData(7)
                    charInstance.addNewData(mkCost - 20)
                }
                R.string.lifeDrain ->{
                    charInstance.addNewData(8)
                    charInstance.addNewData(mkCost - 15)
                }
                R.string.controlData ->{
                    charInstance.addNewData(9)
                    charInstance.addNewData(mkCost - 40)
                }
                R.string.death ->{
                    charInstance.addNewData(10)
                    charInstance.addNewData(mkCost - 50)
                }
                else -> {}
            }

            charInstance.addNewData(primaryCost)
        }
        else {
            charInstance.addNewData(primaryCost)
            charInstance.addNewData(secondaryCost)
            charInstance.addNewData(mkCost)
        }
    }
